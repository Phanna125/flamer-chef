export function getRecipeDisplayTitle(recipe) {
  return recipe?.name?.km || recipe?.name?.en || '';
}

export function getRecipeSecondaryTitle(recipe) {
  if (!recipe) {
    return '';
  }

  return recipe.name.km ? recipe.name.en : '';
}

export function buildRecipeSummary(recipe) {
  if (!recipe) {
    return '';
  }

  const category = translateCategory(recipe.category);
  const leadIngredients = recipe.ingredients
    .slice(0, 3)
    .map((ingredient) => translateIngredientName(ingredient.name))
    .join(' • ');

  return `${getRecipeDisplayTitle(recipe)} ជាមុខម្ហូប${category} ដែលពេញនិយម មានគ្រឿងផ្សំសំខាន់ៗដូចជា ${leadIngredients} និងសាកសមសម្រាប់ការចម្អិននៅផ្ទះ។`;
}

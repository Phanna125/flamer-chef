const fs = require('node:fs/promises');
const path = require('node:path');
const { pathToFileURL } = require('node:url');

function sqlString(value) {
  return `'${String(value).replace(/'/g, "''")}'`;
}

function sqlNullableString(value) {
  return value === null || value === undefined ? 'null' : sqlString(value);
}

function sqlNumber(value) {
  return value === null || value === undefined ? 'null' : String(value);
}

function sqlBoolean(value) {
  return value ? 'true' : 'false';
}

function sqlTextArray(values) {
  if (!Array.isArray(values) || values.length === 0) {
    return 'ARRAY[]::text[]';
  }

  return `ARRAY[${values.map(sqlString).join(', ')}]::text[]`;
}

async function loadRecipes() {
  const recipesModuleUrl = pathToFileURL(path.resolve(__dirname, '../src/data/recipes.js')).href;
  const { recipes } = await import(recipesModuleUrl);
  return recipes;
}

async function main() {
  const recipes = await loadRecipes();
  const outputPath = path.resolve(__dirname, '../supabase/seed/recipes_seed.sql');

  const recipeStatements = recipes.map((recipe) => `
insert into public.recipes (
  id,
  name_en,
  name_km,
  image,
  category,
  difficulty,
  prep_time,
  cook_time,
  servings,
  rating,
  dietary
) values (
  ${sqlString(recipe.id)},
  ${sqlString(recipe.name.en)},
  ${sqlNullableString(recipe.name.km)},
  ${sqlString(recipe.image)},
  ${sqlString(recipe.category)},
  ${sqlString(recipe.difficulty)},
  ${sqlNumber(recipe.prepTime)},
  ${sqlNumber(recipe.cookTime)},
  ${sqlNumber(recipe.servings)},
  ${sqlNumber(recipe.rating)},
  ${sqlTextArray(recipe.dietary)}
);`.trim());

  const ingredientStatements = recipes.flatMap((recipe) =>
    recipe.ingredients.map((ingredient, index) => `
insert into public.recipe_ingredients (
  recipe_id,
  name,
  amount,
  essential,
  sort_order
) values (
  ${sqlString(recipe.id)},
  ${sqlString(ingredient.name)},
  ${sqlNullableString(ingredient.amount)},
  ${sqlBoolean(ingredient.essential)},
  ${index}
);`.trim()),
  );

  const stepStatements = recipes.flatMap((recipe) =>
    recipe.steps.map((step, index) => `
insert into public.recipe_steps (
  recipe_id,
  instruction,
  timer,
  tip,
  sort_order
) values (
  ${sqlString(recipe.id)},
  ${sqlString(step.instruction)},
  ${sqlNumber(step.timer)},
  ${sqlNullableString(step.tip)},
  ${index}
);`.trim()),
  );

  const sql = [
    '-- Generated by scripts/generate-supabase-seed.cjs',
    'begin;',
    'truncate table public.recipe_steps restart identity cascade;',
    'truncate table public.recipe_ingredients restart identity cascade;',
    'truncate table public.recipes cascade;',
    '',
    ...recipeStatements,
    '',
    ...ingredientStatements,
    '',
    ...stepStatements,
    '',
    'commit;',
    '',
  ].join('\n');

  await fs.writeFile(outputPath, sql, 'utf8');
  console.log(`Generated Supabase seed at ${outputPath}`);
}

main().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});
